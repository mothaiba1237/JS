// Sử dụng Math.abs() - Trả về giá trị tuyệt đối của một số
document.writeln(Math.abs(-5.5)); // Kết quả: 5.5 (loại bỏ dấu âm)
document.writeln(Math.abs(10));   // Kết quả: 10 (vì 10 đã là số dương)
// Sử dụng Math.ceil() - Làm tròn lên đến số nguyên gần nhất
document.writeln(Math.ceil(4.4)); // Kết quả: 5 (làm tròn lên từ 4.4 thành 5)
document.writeln(Math.ceil(5.8)); // Kết quả: 6 (làm tròn lên từ 5.8 thành 6)
// Sử dụng Math.floor() - Làm tròn xuống đến số nguyên gần nhất
document.writeln(Math.floor(5.7)); // Kết quả: 5 (làm tròn xuống từ 5.7 thành 5)
document.writeln(Math.floor(6.1)); // Kết quả: 6 (làm tròn xuống từ 6.1 thành 6)
// Sử dụng Math.round() - Làm tròn đến số nguyên gần nhất
document.writeln(Math.round(10.1)); // Kết quả: 10 (vì 10.1 gần 10 hơn)
document.writeln(Math.round(20.6)); // Kết quả: 21 (vì 20.6 gần 21 hơn)
